def add(a, b):
    return a + b

# define subtraction
def subtract(a, b):
    return a - b

# define multiplication
def multiply(a, b):
    return a * b

# define division
def divide(a, b):
    return a / b

# define exponentiation
def power(a, b):
    return a ** b

# define square root
def sqrt(a):
    return a ** 0.5

# define absolute value
def abs(a):
    return a if a >= 0 else -a

